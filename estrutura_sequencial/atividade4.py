#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota1 = float(input("infome sua primeira nota:"))
nota2 = float(input("infome sua segunda nota:"))
nota3 = float(input("infome sua terceira nota:"))
nota4 = float(input("infome sua quarta nota:"))

resultado = (nota1+nota2+nota3+nota4)/4

print("sua media bimestral é",resultado,"!!!!" )