#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.


ariaQuadrado = float(input("informe a aria do quadrado Em METROS:"))

#Realiza a conversão Em Metros quadrado

resultado = ariaQuadrado **2

print(f"o resultado é {resultado} m² e o dobro do valor é {(resultado+resultado)} m²")


