#Faça um Programa que peça dois números e imprima o maior deles.


#entrada de dados 
numero1 = int(input('por favor inforome o primeiro valor:'))
numero2 = int(input('por favor informe o segundo valor:'))

#condicional de verificação e exibição
if numero1 > numero2:
    print('o maior valor foi o primeiro valor' ,numero1)

else:
    print('o maior valor foi  segundo ' ,numero2)
